// Programa para determinar el día de la semana basado en un número

let Dia = 4

if (Dia === 1) {
    console.log("Lunes");
} else if (Dia === 2) {
    console.log("Martes");
} else if (Dia === 3) {
    console.log("Miércoles");
} else if (Dia === 4) {
    console.log("Jueves");
} else if (Dia === 5) {
    console.log("Viernes");
} else if (Dia === 6) {
    console.log("Sábado");
} else if (Dia === 7) {
    console.log("Domingo");
} else {
    console.log("Número inválido. Debe ser entre 1 y 7.");
}